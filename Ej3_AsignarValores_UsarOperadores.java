public class Ej3_AsignarValores_UsarOperadores {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Define variables donde puedas alojar los resultados y prueba usar dos operadores de cada tipo.
        int num1 = 20;
        int num2 = 10;
        int suma = num1 + num2;
        System.out.println("La suma es: " + suma);
        int multiplicacion = num1 * num2;
        System.out.println("La multiplicacion es: " + multiplicacion);
        int resta = num1 - num2;
        System.out.println("La resta es: " + resta);
        double division = num1 / num2;
        System.out.println("La división es: " + division);
        boolean num = num1 > num2;
        System.out.println("La comparación es: " + num);
        num1 ++;
        System.out.println("Incremento " + num1);
        num2 --;
        System.out.println("Decremento " + num2);
        boolean numeros = num1 != num2;
        System.out.println("La comparación es: " + numeros);
        
                
                
        
    }
    
}
